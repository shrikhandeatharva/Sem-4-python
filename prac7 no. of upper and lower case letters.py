string = "PythonLanguage"
upper_count = 0
lower_count = 0
for element in string:
    if element.isupper():
           upper_count +=1
    elif element.islower():
            lower_count += 1
print("Number of uppercase letters in the given string is :  ")
print(upper_count)
print("Number of lowercase letters in the given string is :  ")
print(lower_count)
  
  
  
      
